def addition(num1, num2)
  sum = num1 + num2
  puts sum
  sum
end

def subtraction(num1, num2)
  difference = num2 - num1
  puts difference
  difference
end

def division(num1, num2)
  quotient = num2 / num1
  puts quotient
  quotient
end

def multiplication(num1, num2)
  product = num1 * num2
  puts product
  product
end

def modulo(num1, num2)
  remainder = num2 % num1
  puts remainder
  remainder
end

def square_root(num)
  result = Math.sqrt(num)
  puts result
  result
end